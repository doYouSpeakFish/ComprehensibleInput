/**
 * Whether [ComprehensibleInputTheme] is currently rendering its dark variant. Provided so UI code
 * can react to the active theme without re-deriving it from the system setting or colour luminance.
 */
val LocalIsDarkTheme = staticCompositionLocalOf { false }

@Composable
fun ComprehensibleInputTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable() () -> Unit,
) {
    CompositionLocalProvider(LocalIsDarkTheme provides darkTheme) {
        MaterialTheme(
            colorScheme = if (darkTheme) darkScheme else lightScheme,
            typography = AppTypography,
            content = content,
        )
    }
}
